package day03_EscapeSequences;

public class EscapeSequences {

    public static void main(String[] args) {
        System.out.println("Java \nPython \nC#"); // \n: Advances the cursor to the next line for subsequent printing

        System.out.println("Hello Cydeo \nHow are you all today? \nIt's nice to see you all \nWhat class do we have next week?");

        System.out.println("----------------------------------");

        System.out.println("\thow are you doing ?"); // \t: tab (leaves 4 spaces)

        System.out.println("/ \\"); // in order to print one backward slash we need to give two backward slashes

        System.out.println("My favourite TV show is \"Game of Thrones\"");
    }
}
