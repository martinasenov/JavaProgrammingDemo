package day17_White_DoWhile;

public class LoopPractices {

    public static void main(String[] args) {

        for (int i = 0; i <=10 ; i++) {

            System.out.println(i);
        }

        System.out.println("-------------------------");


        int j=0;
        while(j<10){
            j++;

            System.out.println(j);
        }
    }
}
